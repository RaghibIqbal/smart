package com.Logicjavap.java;

public class EvenPosition {

	public static void main(String[] args) {

       int arr[]= {1,2,3,4,5};
       System.out.println("print element of even position");
       for(int i=1;i<arr.length;i=i+2)
       {
    	   System.out.println(arr[i]+" ");
       }
	}

}
